def main():
    session = SessionLocal()
    try:
        merchants = session.query(Merchant).all()
        category_index: dict[str, list[Merchant]] = {}
        for m in merchants:
            category_index.setdefault(m.homepage_category, []).append(m)

        total_added = 0
        for batch_name, batch_data in [("batch1", EXTRA_DISHES_BY_CATEGORY), ("batch2", EXTRA_DISHES_BATCH2), ("batch3", EXTRA_DISHES_BATCH3)]:
            for cat, dishes in batch_data.items():
                target_merchants = category_index.get(cat, [])
                if not target_merchants:
                    print(f"  WARN: no merchants for category '{cat}'")
                    continue
                for i, (name, desc, price, tags, flavor) in enumerate(dishes):
                    merchant = target_merchants[i % len(target_merchants)]
                    cat_obj = session.query(DishCategory).filter(
                        DishCategory.merchant_id == merchant.id
                    ).first()
                    if not cat_obj:
                        continue
                    dish_obj = Dish(
                        merchant_id=merchant.id,
                        category_id=cat_obj.id,
                        name=name,
                        description=f"{desc}，整体{flavor}。",
                        price=price,
                        tags=tags,
                        cuisine_type=cat,
                        flavor_profile=flavor,
                        ingredients=[name[-2:]] if len(name) > 1 else ["主料"],
                        allergens=[],
                        cooking_method="现制",
                        is_recommended=(i == 0),
                        is_available=True,
                    )
                    session.add(dish_obj)
                    total_added += 1

        session.commit()
        # Count
        count = session.execute(text("SELECT COUNT(*) FROM dishes")).scalar()
        print(f"Added {total_added} dishes. Total dishes: {count}")
    finally:
        session.close()


if __name__ == "__main__":
    main()
